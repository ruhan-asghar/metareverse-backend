def seed_posts_and_media(
    cur,
    org_uuid: uuid.UUID,
    clerk_org_id: str,
    pages: list[dict],
    users: list[uuid.UUID],
    posting_ids: list[uuid.UUID],
    rnd: random.Random,
    target_posts: int,
) -> int:
    """Seed NUM_POSTS posts with media and return how many we inserted/upserted."""
    now = datetime.now(timezone.utc)
    count = 0
    for i in range(target_posts):
        page = rnd.choice(pages)
        platform = page["platform"]
        media_type = rnd.choice(MEDIA_TYPES)
        status = pick_status(rnd)
        author = rnd.choice(users)
        posting_used = rnd.choice(posting_ids) if platform == "facebook" else None

        post_id = det_uuid(f"seed-{clerk_org_id}-post-{i}")
        caption_text = f"Demo post {str(post_id)[:6]} about {page['name']}"

        caption_fb = caption_text if platform == "facebook" else None
        caption_ig = caption_text if platform == "instagram" else None
        caption_th = caption_text if platform == "threads" else None

        publish_fb = platform == "facebook"
        publish_ig = platform == "instagram"
        publish_th = platform == "threads"

        scheduled_at = now - timedelta(hours=rnd.randint(-72, 24 * 30))
        published_at = scheduled_at + timedelta(minutes=rnd.randint(1, 45)) if status == "published" else None
        failed_at = now - timedelta(hours=rnd.randint(1, 48)) if status.startswith("failed") or status == "reconnect_required" else None
        failed_category = None
        failure_reason = None
        if status == "failed_temporary":
            failed_category = "temporary_issue"
            failure_reason = "Graph API returned 500"
        elif status == "failed_needs_editing":
            failed_category = "needs_editing"
            failure_reason = "Caption exceeded platform limit"
        elif status == "reconnect_required":
            failed_category = "reconnect_needed"
            failure_reason = "Access token expired mid-publish"

        file_hash = fake_hash(f"{clerk_org_id}-post-{i}") if media_type != "text" else None

        cur.execute(
            """
            INSERT INTO posts (
                id, org_id, page_id, created_by, posting_id_used,
                status, failed_category, media_type,
                caption_facebook, caption_instagram, caption_threads,
                publish_to_facebook, publish_to_instagram, publish_to_threads,
                scheduled_at, published_at, failed_at, failure_reason,
                retry_count, file_hash, is_outside_active_hours
            )
            VALUES (%s, %s, %s, %s, %s,
                    %s, %s, %s,
                    %s, %s, %s,
                    %s, %s, %s,
                    %s, %s, %s, %s,
                    %s, %s, %s)
            ON CONFLICT (id) DO UPDATE
              SET status = EXCLUDED.status,
                  failed_category = EXCLUDED.failed_category,
                  media_type = EXCLUDED.media_type,
                  caption_facebook = EXCLUDED.caption_facebook,
                  caption_instagram = EXCLUDED.caption_instagram,
                  caption_threads = EXCLUDED.caption_threads,
                  publish_to_facebook = EXCLUDED.publish_to_facebook,
                  publish_to_instagram = EXCLUDED.publish_to_instagram,
                  publish_to_threads = EXCLUDED.publish_to_threads,
                  scheduled_at = EXCLUDED.scheduled_at,
                  published_at = EXCLUDED.published_at,
                  failed_at = EXCLUDED.failed_at,
                  failure_reason = EXCLUDED.failure_reason,
                  file_hash = EXCLUDED.file_hash
            """,
            (
                str(post_id), str(org_uuid), str(page["id"]), str(author),
                str(posting_used) if posting_used else None,
                status, failed_category, media_type,
                caption_fb, caption_ig, caption_th,
                publish_fb, publish_ig, publish_th,
                scheduled_at, published_at, failed_at, failure_reason,
                rnd.randint(0, 3), file_hash, rnd.random() < 0.1,
            ),
        )
        count += 1

        # Attach one media row for photo/reel posts
        if media_type != "text":
            media_id = det_uuid(f"seed-{clerk_org_id}-post-{i}-media-0")
            file_key = f"seed/{clerk_org_id}/{post_id}/0.jpg"
            file_url = f"https://r2.metareverse.xyz/{file_key}"
            cur.execute(
                """
                INSERT INTO post_media (
                    id, post_id, org_id, file_url, file_key, file_hash,
                    file_size, mime_type, sort_order
                )
                VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s)
                ON CONFLICT (id) DO NOTHING
                """,
                (
                    str(media_id), str(post_id), str(org_uuid),
                    file_url, file_key, file_hash,
                    1024, "image/jpeg" if media_type == "photo" else "video/mp4",
                    0,
                ),
            )

        # Approval record for a chunk of pending/reviewed posts
        if status in ("pending_approval", "changes_requested") and rnd.random() < 0.7:
            action = "approved" if status != "changes_requested" else "changes_requested"
            approval_id = det_uuid(f"seed-{clerk_org_id}-approval-{i}")
            cur.execute(
                """
                INSERT INTO approvals (id, post_id, reviewed_by, action, comment)
                VALUES (%s, %s, %s, %s, %s)
                ON CONFLICT (id) DO NOTHING
                """,
                (
                    str(approval_id), str(post_id), str(rnd.choice(users)), action,
                    "Looks great" if action == "approved" else "Please refresh the thumbnail",
                ),
            )

    return count


def seed_insights_and_revenue(
    cur,
    org_uuid: uuid.UUID,
    pages: list[dict],
    rnd: random.Random,
    days: int = INSIGHTS_DAYS,
) -> tuple[int, int]:
    today = date.today()
    insight_rows: list[tuple] = []
    revenue_rows: list[tuple] = []
    for page in pages:
        page_id = str(page["id"])
        for d in range(days):
            day = today - timedelta(days=d)
            views = rnd.randint(1_000, 120_000)
            viewers = int(views * rnd.uniform(0.4, 0.8))
            follows = rnd.randint(0, 400)
            unfollows = rnd.randint(0, 120)
            visits = rnd.randint(100, 9_000)
            interactions = rnd.randint(50, 12_000)
            link_clicks = rnd.randint(0, 2_500)
            video_views = rnd.randint(0, 80_000)
            watch_time = rnd.randint(0, 300_000)
            reactions = rnd.randint(10, 9_000)
            comments = rnd.randint(0, 1_200)
            shares = rnd.randint(0, 900)
            insight_rows.append((
                page_id, str(org_uuid), day, day,
                views, viewers, follows, unfollows, visits, interactions,
                link_clicks, video_views, watch_time, reactions, comments, shares,
            ))

            reels_cents = rnd.randint(0, 15_000)
            photos_cents = rnd.randint(0, 6_000)
            stories_cents = rnd.randint(0, 3_000)
            text_cents = rnd.randint(0, 800)
            total_cents = reels_cents + photos_cents + stories_cents + text_cents
            revenue_rows.append((
                page_id, str(org_uuid), day,
                total_cents, reels_cents, photos_cents, stories_cents, text_cents,
                views, "USD",
            ))

    execute_values(
        cur,
        """
        INSERT INTO page_insights (
            page_id, org_id, period_start, period_end,
            views, viewers, follows, unfollows, visits, interactions,
            link_clicks, video_views, watch_time_seconds, reactions, comments, shares
        )
        VALUES %s
        ON CONFLICT (page_id, period_start, period_end) DO UPDATE
          SET views = EXCLUDED.views,
              viewers = EXCLUDED.viewers,
              follows = EXCLUDED.follows,
              unfollows = EXCLUDED.unfollows,
              visits = EXCLUDED.visits,
              interactions = EXCLUDED.interactions,
              link_clicks = EXCLUDED.link_clicks,
              video_views = EXCLUDED.video_views,
              watch_time_seconds = EXCLUDED.watch_time_seconds,
              reactions = EXCLUDED.reactions,
              comments = EXCLUDED.comments,
              shares = EXCLUDED.shares
        """,
        insight_rows,
        page_size=500,
    )

    execute_values(
        cur,
        """
        INSERT INTO revenue_records (
            page_id, org_id, date,
            total_cents, reels_cents, photos_cents, stories_cents, text_cents,
            views, currency
        )
        VALUES %s
        ON CONFLICT (page_id, date) DO UPDATE
          SET total_cents = EXCLUDED.total_cents,
              reels_cents = EXCLUDED.reels_cents,
              photos_cents = EXCLUDED.photos_cents,
              stories_cents = EXCLUDED.stories_cents,
              text_cents = EXCLUDED.text_cents,
              views = EXCLUDED.views
        """,
        revenue_rows,
        page_size=500,
    )

    return len(insight_rows), len(revenue_rows)


def seed_invite_links(
    cur,
    org_uuid: uuid.UUID,
    clerk_org_id: str,
    invited_by: uuid.UUID,
    batch_ids: list[uuid.UUID],
) -> None:
    now = datetime.now(timezone.utc)
    demos = [
        # email, roles, status, expires_offset_days, accepted
        ("pending.approver@demo.local", ["approver"], "pending", 7, None),
        ("expired.publisher@demo.local", ["publisher"], "expired", -2, None),
        ("accepted.manager@demo.local", ["manager"], "accepted", 5, now - timedelta(days=1)),
    ]
    for idx, (email, roles, status, exp_days, accepted_at) in enumerate(demos):
        token = f"seedtok-{clerk_org_id}-{idx}-{hashlib.sha1(email.encode()).hexdigest()[:16]}"
        expires_at = now + timedelta(days=exp_days)
        cur.execute(
            """
            INSERT INTO invite_links (
                org_id, token, email, roles, batch_ids, invited_by,
                expires_at, accepted_at, status
            )
            VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s)
            ON CONFLICT (token) DO UPDATE
              SET status = EXCLUDED.status,
                  accepted_at = EXCLUDED.accepted_at,
                  expires_at = EXCLUDED.expires_at
            """,
            (
                str(org_uuid), token, email, roles,
                [str(b) for b in batch_ids[:2]],
                str(invited_by), expires_at, accepted_at, status,
            ),
        )


