"""Rich demo seed data generator for MetaReverse.

Generates a full slice of realistic data for one organization:
  - 1 organization (upsert by clerk_org_id)
  - 5 users (1 owner + 4 team members)
  - 5 batches
  - 35 pages (~7 per batch, mixed FB/IG/Threads)
  - 8 posting IDs with assignments
  - 120+ posts across many statuses
  - 90 days of page_insights per page
  - 90 days of revenue_records per page
  - team_members, invite_links, approvals

Safe to re-run: everything is keyed by UNIQUE constraints and UPSERTs.

USAGE:
    python -m scripts.generate_seed_data --org-id org_test --email demo@metareverse.xyz

The --org-id value is a Clerk-style organization identifier (string).
It maps to organizations.clerk_org_id. The underlying UUID PK is
generated by Postgres and captured for all FK inserts.
"""
