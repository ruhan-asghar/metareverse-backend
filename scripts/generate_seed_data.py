PAGES_PER_BATCH = 7
NUM_BATCHES = 5
NUM_POSTING_IDS = 8
NUM_EXTRA_USERS = 4
NUM_POSTS = 130
INSIGHTS_DAYS = 90
SEED_NAMESPACE = uuid.NAMESPACE_DNS

BATCH_SPECS = [
    ("Lifestyle Network", "#f97316", "Lifestyle, wellness, travel pages"),
    ("Gaming Collective", "#8b5cf6", "Gaming and esports pages"),
    ("Foodie Portfolio", "#ef4444", "Food, recipes, restaurants"),
    ("Tech & Startups", "#3b82f6", "Tech news, startup culture"),
    ("Sports Hub", "#10b981", "Sports, fitness, training"),
]

PLATFORMS = ["facebook", "instagram", "threads"]
MEDIA_TYPES = ["photo", "reel", "text"]

# Distribution weights for post status (~sum to 1.0)
POST_STATUS_WEIGHTS = [
    ("published", 0.55),
    ("queued", 0.15),
    ("draft", 0.10),
    ("pending_approval", 0.05),
    ("failed_temporary", 0.05),
    ("failed_needs_editing", 0.04),
    ("reconnect_required", 0.03),
    ("paused", 0.02),
    ("changes_requested", 0.01),
]


