def run(clerk_org_id: str, owner_email: str, seed: int = 42) -> dict:
    rnd = random.Random(seed)
    settings = get_settings()
    conn = psycopg2.connect(settings.database_url)
    conn.autocommit = False
    stats: dict = {}

    try:
        with conn.cursor(cursor_factory=RealDictCursor) as cur:
            # 1. Organization
            org_uuid = upsert_organization(
                cur, clerk_org_id, name=f"Seed Org {clerk_org_id}"
            )
            stats["org_uuid"] = str(org_uuid)

            # 2. Users (owner + extras)
            owner_id = upsert_user(
                cur,
                clerk_user_id=f"clerk_{seed}_owner",
                org_uuid=org_uuid,
                email=owner_email,
                first_name="Owner",
                last_name="Zero",
            )
            extra_user_ids: list[uuid.UUID] = []
            for i in range(1, NUM_EXTRA_USERS + 1):
                uid = upsert_user(
                    cur,
                    clerk_user_id=f"clerk_{seed}_{i}",
                    org_uuid=org_uuid,
                    email=f"user{i}@demo.local",
                    first_name="User",
                    last_name=str(i),
                )
                extra_user_ids.append(uid)
            all_users = [owner_id, *extra_user_ids]
            stats["users"] = len(all_users)

            # 3. Batches
            batch_ids: list[uuid.UUID] = []
            for name, color, desc in BATCH_SPECS[:NUM_BATCHES]:
                bid = upsert_batch(cur, org_uuid, name, color, desc)
                batch_ids.append(bid)
            stats["batches"] = len(batch_ids)

            # 4. Posting IDs
            posting_ids: list[uuid.UUID] = []
            active_posting_ids: list[uuid.UUID] = []
            for i in range(NUM_POSTING_IDS):
                fb_uid = f"fb_seed_{clerk_org_id}_{i}"
                name = f"Posting ID #{i + 1}"
                enc = encrypt_token(f"mock_posting_token_{clerk_org_id}_{i}")
                # Spread health + statuses for realistic fixtures
                health = rnd.randint(25, 100)
                reach = rnd.randint(50_000, 2_500_000)
                status = "active"
                if i == NUM_POSTING_IDS - 1:
                    status = "retired"
                elif i == NUM_POSTING_IDS - 2:
                    status = "expired"
                pid = upsert_posting_id(
                    cur, org_uuid, fb_uid, name, enc, health, reach, status
                )
                posting_ids.append(pid)
                if status == "active":
                    active_posting_ids.append(pid)
            stats["posting_ids"] = len(posting_ids)

            if not active_posting_ids:
                active_posting_ids = posting_ids[: max(1, NUM_POSTING_IDS - 2)]

            # 5. Pages (~7 per batch, cycle through platforms)
            pages: list[dict] = []
            page_idx = 0
            for batch_id in batch_ids:
                for _ in range(PAGES_PER_BATCH):
                    platform = PLATFORMS[page_idx % len(PLATFORMS)]
                    platform_page_id = f"pp_{clerk_org_id}_{page_idx}"
                    page_name = f"Seed Page {page_idx + 1}"
                    enc = encrypt_token(f"mock_token_page_{page_idx}")
                    interval = rnd.choice([1, 2, 3, 4, 6, 8])
                    tz = rnd.choice(
                        ["UTC", "America/New_York", "Europe/London", "Asia/Karachi"]
                    )
                    # Spread statuses a bit
                    if page_idx % 15 == 0:
                        pstatus = "token_expiring"
                    elif page_idx % 23 == 0:
                        pstatus = "token_expired"
                    elif page_idx % 31 == 0:
                        pstatus = "paused"
                    else:
                        pstatus = "ready"
                    mon = rnd.choice(["enrolled", "not_enrolled", "ineligible"])
                    require_approval = rnd.random() < 0.3
                    pid_row = upsert_page(
                        cur,
                        org_uuid,
                        batch_id,
                        platform,
                        platform_page_id,
                        page_name,
                        enc,
                        interval,
                        tz,
                        pstatus,
                        mon,
                        require_approval,
                    )
                    pages.append({
                        "id": pid_row,
                        "platform": platform,
                        "name": page_name,
                        "batch_id": batch_id,
                    })
                    page_idx += 1
            stats["pages"] = len(pages)

            # 6. Assign 2 posting IDs per page (round robin) for FB-connected pages
            for idx, page in enumerate(pages):
                pool = active_posting_ids or posting_ids
                primary = pool[idx % len(pool)]
                secondary = pool[(idx + 1) % len(pool)]
                upsert_page_posting_assignment(cur, page["id"], primary, 0)
                if secondary != primary:
                    upsert_page_posting_assignment(cur, page["id"], secondary, 1)

            # 7. Team memberships
            # Owner covers everything; others get subsets of batches + role mix
            upsert_team_member(cur, org_uuid, owner_id, ["owner"], batch_ids, None)
            role_rotation = [
                ["manager"],
                ["publisher"],
                ["approver"],
                ["analyst", "publisher"],
            ]
            for i, uid in enumerate(extra_user_ids):
                roles = role_rotation[i % len(role_rotation)]
                subset = batch_ids[: max(1, (i % len(batch_ids)) + 1)]
                upsert_team_member(cur, org_uuid, uid, roles, subset, owner_id)

            # 8. Posts + media + approvals
            stats["posts"] = seed_posts_and_media(
                cur,
                org_uuid,
                clerk_org_id,
                pages,
                all_users,
                posting_ids,
                rnd,
                target_posts=NUM_POSTS,
            )

            # 9. Page insights + revenue (90 days each)
            insight_n, revenue_n = seed_insights_and_revenue(
                cur, org_uuid, pages, rnd, days=INSIGHTS_DAYS
            )
            stats["page_insights"] = insight_n
            stats["revenue_records"] = revenue_n

            # 10. Invite links
            seed_invite_links(cur, org_uuid, clerk_org_id, owner_id, batch_ids)

        conn.commit()
    except Exception:
        conn.rollback()
        raise
    finally:
        conn.close()

    return stats


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        description=(
            "Generate rich demo seed data for MetaReverse. "
            "The --org-id value is a Clerk-style string identifier and is stored "
            "as organizations.clerk_org_id. The internal UUID PK is generated by "
            "Postgres and used for all downstream FK inserts."
        )
    )
    parser.add_argument(
        "--org-id",
        required=True,
        help="Clerk-style org identifier (string). Maps to organizations.clerk_org_id.",
    )
    parser.add_argument(
        "--email",
        required=True,
        help="Owner user's email address.",
    )
    parser.add_argument(
        "--seed",
        type=int,
        default=42,
        help="Random seed for deterministic-ish output (default: 42).",
    )
    args = parser.parse_args(argv)

    stats = run(args.org_id, args.email, seed=args.seed)
    print("[seed] done:", stats)
    return 0


