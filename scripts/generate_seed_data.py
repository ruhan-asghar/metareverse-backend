def det_uuid(*parts: str) -> uuid.UUID:
    """Deterministic UUID5 from any set of string parts."""
    return uuid.uuid5(SEED_NAMESPACE, "|".join(parts))


def pick_status(rnd: random.Random) -> str:
    r = rnd.random()
    acc = 0.0
    for name, w in POST_STATUS_WEIGHTS:
        acc += w
        if r <= acc:
            return name
    return "published"


def fake_hash(seed: str) -> str:
    return hashlib.sha256(seed.encode()).hexdigest()


