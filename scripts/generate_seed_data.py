def upsert_organization(cur, clerk_org_id: str, name: str) -> uuid.UUID:
    cur.execute(
        """
        INSERT INTO organizations (clerk_org_id, name, plan)
        VALUES (%s, %s, 'free')
        ON CONFLICT (clerk_org_id) DO UPDATE
          SET name = EXCLUDED.name
        RETURNING id
        """,
        (clerk_org_id, name),
    )
    return cur.fetchone()["id"]


def upsert_user(
    cur,
    clerk_user_id: str,
    org_uuid: uuid.UUID,
    email: str,
    first_name: str,
    last_name: str,
) -> uuid.UUID:
    cur.execute(
        """
        INSERT INTO users (clerk_user_id, org_id, email, first_name, last_name, is_active)
        VALUES (%s, %s, %s, %s, %s, true)
        ON CONFLICT (clerk_user_id) DO UPDATE
          SET email = EXCLUDED.email,
              first_name = EXCLUDED.first_name,
              last_name = EXCLUDED.last_name,
              org_id = EXCLUDED.org_id
        RETURNING id
        """,
        (clerk_user_id, str(org_uuid), email, first_name, last_name),
    )
    return cur.fetchone()["id"]


def upsert_batch(
    cur, org_uuid: uuid.UUID, name: str, color: str, description: str
) -> uuid.UUID:
    cur.execute(
        """
        INSERT INTO batches (org_id, name, color, description)
        VALUES (%s, %s, %s, %s)
        ON CONFLICT (org_id, name) DO UPDATE
          SET color = EXCLUDED.color,
              description = EXCLUDED.description
        RETURNING id
        """,
        (str(org_uuid), name, color, description),
    )
    return cur.fetchone()["id"]


def upsert_posting_id(
    cur,
    org_uuid: uuid.UUID,
    facebook_user_id: str,
    name: str,
    enc_token: bytes,
    health_score: int,
    reach_28d: int,
    status: str,
) -> uuid.UUID:
    token_expires = datetime.now(timezone.utc) + timedelta(days=55)
    cur.execute(
        """
        INSERT INTO posting_ids (
            org_id, facebook_user_id, name, encrypted_access_token,
            token_expires_at, status, health_score, reach_28d, last_used_at
        )
        VALUES (%s, %s, %s, %s, %s, %s, %s, %s, now())
        ON CONFLICT (org_id, facebook_user_id) DO UPDATE
          SET name = EXCLUDED.name,
              health_score = EXCLUDED.health_score,
              reach_28d = EXCLUDED.reach_28d,
              status = EXCLUDED.status,
              last_used_at = EXCLUDED.last_used_at
        RETURNING id
        """,
        (
            str(org_uuid),
            facebook_user_id,
            name,
            psycopg2.Binary(enc_token),
            token_expires,
            status,
            health_score,
            reach_28d,
        ),
    )
    return cur.fetchone()["id"]


def upsert_page(
    cur,
    org_uuid: uuid.UUID,
    batch_id: uuid.UUID,
    platform: str,
    platform_page_id: str,
    name: str,
    enc_token: bytes,
    interval: int,
    timezone_name: str,
    status: str,
    monetization_status: str,
    require_approval: bool,
) -> uuid.UUID:
    token_expires = datetime.now(timezone.utc) + timedelta(days=45)
    cur.execute(
        """
        INSERT INTO pages (
            org_id, batch_id, platform, platform_page_id, name,
            timezone, post_interval_hours,
            active_hours_start, active_hours_end,
            require_approval, rotation_mode,
            monetization_status, status,
            encrypted_access_token, token_expires_at,
            follower_count, follower_count_updated_at,
            is_active
        )
        VALUES (%s, %s, %s, %s, %s,
                %s, %s,
                %s, %s,
                %s, 'round_robin',
                %s, %s,
                %s, %s,
                %s, now(),
                true)
        ON CONFLICT (org_id, platform, platform_page_id) DO UPDATE
          SET name = EXCLUDED.name,
              batch_id = EXCLUDED.batch_id,
              timezone = EXCLUDED.timezone,
              post_interval_hours = EXCLUDED.post_interval_hours,
              active_hours_start = EXCLUDED.active_hours_start,
              active_hours_end = EXCLUDED.active_hours_end,
              require_approval = EXCLUDED.require_approval,
              monetization_status = EXCLUDED.monetization_status,
              status = EXCLUDED.status,
              encrypted_access_token = EXCLUDED.encrypted_access_token,
              token_expires_at = EXCLUDED.token_expires_at,
              follower_count = EXCLUDED.follower_count
        RETURNING id
        """,
        (
            str(org_uuid),
            str(batch_id),
            platform,
            platform_page_id,
            name,
            timezone_name,
            interval,
            time(8, 0),
            time(22, 0),
            require_approval,
            monetization_status,
            status,
            psycopg2.Binary(enc_token),
            token_expires,
            random.randint(2_000, 500_000),
        ),
    )
    return cur.fetchone()["id"]


def upsert_page_posting_assignment(
    cur, page_id: uuid.UUID, posting_id: uuid.UUID, sort_order: int
) -> None:
    cur.execute(
        """
        INSERT INTO page_posting_id_assignments (page_id, posting_id_id, sort_order)
        VALUES (%s, %s, %s)
        ON CONFLICT (page_id, posting_id_id) DO NOTHING
        """,
        (str(page_id), str(posting_id), sort_order),
    )


def upsert_team_member(
    cur,
    org_uuid: uuid.UUID,
    user_id: uuid.UUID,
    roles: list[str],
    batch_ids: list[uuid.UUID],
    invited_by: uuid.UUID | None,
) -> None:
    cur.execute(
        """
        INSERT INTO team_members (org_id, user_id, roles, batch_ids, status, invited_by, joined_at)
        VALUES (%s, %s, %s, %s, 'active', %s, now())
        ON CONFLICT (org_id, user_id) DO UPDATE
          SET roles = EXCLUDED.roles,
              batch_ids = EXCLUDED.batch_ids,
              status = EXCLUDED.status
        """,
        (
            str(org_uuid),
            str(user_id),
            roles,
            [str(b) for b in batch_ids],
            str(invited_by) if invited_by else None,
        ),
    )


